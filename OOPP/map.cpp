Map::Map(SDL_Renderer* rend) {
	tile1 = TextureManager::LoadTexture("assets/tile1.png",rend);
	LoadMap(lvl0);
	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;
	dest.x = dest.y = 0;
}

void Map::LoadMap(int arr[20][25]) {
	for (int i = 0; i < 20; i++)
		for (int j = 0; j < 25; j++)
			map[i][j] = arr[i][j];
}
void Map::DrawMap(SDL_Renderer* rend) {
	int type = 0;
	for (int i = 0; i < 20; i++)
		for (int j = 0; j < 25; j++) {
			type = map[i][j];

			dest.x = j * 32;
			dest.y = i * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(tile1, src, dest,rend);
				break;
			default:
				break;
			}
		}
			
}